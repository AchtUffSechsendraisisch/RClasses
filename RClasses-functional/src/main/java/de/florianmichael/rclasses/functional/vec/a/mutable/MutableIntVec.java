package de.florianmichael.rclasses.functional.vec.a.mutable;

import de.florianmichael.rclasses.functional.vec.a.IntVec;

public final class MutableIntVec extends IntVec {
    private int x, y;

    MutableIntVec() {
        this(0, 0);
    }

    public MutableIntVec(final int x, final int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public int getX() {
        return this.x;
    }

    @Override
    public int getY() {
        return this.y;
    }

    @Override
    public void setX(final int x) {
        this.x = x;
    }

    @Override
    public void setY(final int y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return String.format(
                "MutableIntVec{x=%d, y=%d}",
                this.getX(),
                this.getY()
        );
    }
}
